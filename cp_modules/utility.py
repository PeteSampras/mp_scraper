colors = {
        'blue': '\033[94m',
        'pink': '\033[95m',
        'green': '\033[92m',
        'warning' : '\033[93m',
        'fail' : '\033[91m',
        'ENDC' : '\033[0m',
        'bold' : '\033[1m',
        'underline' : '\033[4m',
        }
 
def colorize(string, color):
    if not color in colors: return string
    return colors[color] + string + '\033[0m'